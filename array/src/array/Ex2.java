package array;

public class Ex2 {
	public static void main(String[] args) {
		String str ="3";
		System.out.println(str.charAt(0)-'0'); // 숫자3 문자열을 문자로 바꿈
		System.out.println(str.charAt(0)+1); //52 문자3과 1더하기결과
		System.out.println(Integer.parseInt("3")+1);//4
		System.out.println("3"+1);//31
		
	}
	
	
}
